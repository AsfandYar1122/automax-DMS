        private void GenerateFile(string localFilePath)
        {
            try
            {
                string sqlQuery = @"select VehicleWizards.VIN,  
VehicleWizards.StockNumber as 'Stock', 
Years.YearName as 'Year',
Makers.Name as 'Make',
 AutoModels.ModelName as 'Model',
VehicleTypes.Type as 'Body',
SubModels.ModelName as 'Trim',
VehicleWizards.MilageValue as Mileage,
'' as Price,
AutoExteriorColors.ExteriorColor as 'ExteriorColor',
AutoInteriorColors.InteriorColor as 'InteriorColor',
AutoUsedStatus.UsedStatus as 'Condition'

from VehicleWizards
left JOIN AutoUsedStatus on VehicleWizards.AutoUsedStatusID = AutoUsedStatus.AutoUsedStatusID
left JOIN Years on VehicleWizards.YearID = Years.YearID
left JOIN Makers on VehicleWizards.MakerID = Makers.MakerID
left JOIN AutoModels on VehicleWizards.AutoModelID = AutoModels.AutoModelID
left JOIN SubModels on VehicleWizards.SubModelID = SubModels.SubModelID
left JOIN AutoExteriorColors on VehicleWizards.AutoExteriorColorID = AutoExteriorColors.AutoExteriorColorID
left JOIN AutoInteriorColors on VehicleWizards.AutoInteriorColorID = AutoInteriorColors.AutoInteriorColorID
